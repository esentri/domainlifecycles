package io.domainlifecycles.events.publish;

import io.domainlifecycles.domain.types.DomainEvent;

/**
 * An abstract class that serves as a base for implementing transactional domain event publishers.
 * Subclasses must implement the 'send' method to handle the actual sending of domain events.
 *
 * @author Mario Herb
 */
public abstract class AbstractTransactionalDomainEventPublisher implements DomainEventPublisher{

    /**
     * Sends the given DomainEvent in specific transaction phase.
     *
     * @param domainEvent the DomainEvent to be sent
     */
    abstract protected void send(DomainEvent domainEvent);

}
