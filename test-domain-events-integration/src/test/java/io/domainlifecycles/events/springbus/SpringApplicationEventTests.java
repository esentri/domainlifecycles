package io.domainlifecycles.events.springbus;

import io.domainlifecycles.events.ADomainEvent;
import io.domainlifecycles.events.api.DomainEvents;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.test.annotation.DirtiesContext;

import static org.assertj.core.api.Assertions.assertThat;

@SpringBootTest(classes = TestApplicationSpringBus.class)
@DirtiesContext
public class SpringApplicationEventTests {

    @Autowired
    private ListenerService listenerService;

    @Test
    public void testIntegration() {
        var cnt = listenerService.getCnt();
        //when
        var evt = new ADomainEvent("TestSpring");
        DomainEvents.publish(evt);

        //then
        assertThat(listenerService.getCnt()).isEqualTo(cnt + 1);
    }

}
