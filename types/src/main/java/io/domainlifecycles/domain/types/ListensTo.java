package io.domainlifecycles.domain.types;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marker annotation to mark methods
 * in {@link Entity} instances, {@link Repository} or {@link DomainService} instances,
 * that listen to {@link DomainEvent}s.
 *
 * @author Mario Herb
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.METHOD})
public @interface ListensTo {

    /**
     * The {@link DomainEvent} type that is listened to by the marked method
     *
     * @return the specified DomainEvent class
     */
    Class<? extends DomainEvent> domainEventType();
}
